'''
HashLookUp
Original hash code reverse engineered from NOLF PS2 rez module
Ported to Python, it relies on overflowing signed integers (signed int32), hence the ctypes!
'''
class HashLookUp(object):
    def __init__(self, magic_number):
        self._magic_number = magic_number
        pass

    def lookup_hash(self, hash_value, category):
        for string in HASH_LOOKUP[category]:

            hashed = self.hash(string)
            if hashed == c_int(hash_value).value:
                return string

        return None

    def hash(self, name):
        hash_value = c_int(0)
        for char in name:
            char = char.upper()
            byte_char = ord(char)
            byte_char = byte_char & 0xFF
            if (byte_char == 0x2F):
                byte_char = 0x5C
            hash_value.value = hash_value.value + c_int(byte_char).value + hash_value.value * c_int(self._magic_number).value
        return hash_value.value
    
    def get_hash_for_name(self, name, context):
        """
        Get the hash value for a name in a given context.
        This is essentially the reverse of lookup_hash.
        
        Args:
            name (str): The name to find the hash for
            context (str): The context to search in ('pieces', 'nodes', 'sockets', etc.)
            
        Returns:
            int: The hash value, or None if not found
        """
        # Not all hash dictionaries have been implemented yet
        if not hasattr(self, f"_{context}_dict"):
            return None
            
        # Get the appropriate dictionary for this context
        context_dict = getattr(self, f"_{context}_dict")
        
        # Search for the name in the values, return the corresponding key
        for hash_val, hash_name in context_dict.items():
            if hash_name == name:
                return hash_val
                
        return None
    
